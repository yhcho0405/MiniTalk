#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		n;

	n = 0;
	while (s[n])
		n++;
	while (n > -1 && s[n] != (char)c)
		n--;
	if (n == -1)
		return ((char *)0);
	return ((char *)(s + n));
}
